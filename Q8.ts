// Program#8: Write a program that checks if the givin year is leap year or not
function leapYear(GivenYear: number) {
  if (GivenYear % 4 === 0) {
    console.log(`The given Year ${GivenYear} is a leap year`);
  } else {
    console.log(`The given Year ${GivenYear} is a leap year`);
  }
}

leapYear(2000);
